package com.tj.ex3_math;

public class Ex01_Math {
	public static void main(String[] args) {
		System.out.println("2의 3승 :" + Math.pow(2, 3));
		System.out.println("-9.9의 절대값 : "+Math.abs(-9.9));
		System.out.println("16의 제곱근 : " + (int)(Math.sqrt(16)));
		System.out.println("4와 7중 작은값 : "+Math.min(4, 7));
		System.out.println("4와 7중 큰값 : "+Math.max(4, 7));
		System.out.println("sin(파이) = "+Math.sin(Math.PI));
		
		
	}
}
